#Big o notation is used to measure how much running time or space is
#required for your program as your input size grows.
#-------------------------------------------
# 1. when the time increases as the computation increases(e.g for loop), 
# we say the big O is O(n).

# 2. when the time remains the same even though the input size increases (operations using func)
# we say it is constant, thus O(1)

#3. when you have a for loop that contains a sub-forloop with an operaion, 
# we say it is O(n^2)

#4. when I am doing a search algorithm that requires I do several iterations,
# we say it is O(logn) where n=number of iterations
